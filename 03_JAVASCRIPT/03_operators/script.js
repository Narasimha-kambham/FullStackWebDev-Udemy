// Arithmetic Operators
console.log(2 + 3); //5
console.log(8 - 4); //4
console.log(5 * 4); //20
console.log(4 / 4); //1
console.log(4 % 4); //0
console.log(3 ** 3); //27

// Assignment Operators
let a = 5;
console.log(a); //5
a += 2;
console.log(a); //7
a -= 4;
console.log(a); //3
a *= 4;
console.log(a); //12
a /= 4;
console.log(a); //3

// Comparison Operators
let b = "5";
let c = 5;
console.log(b == c); //true
console.log(b === c); //false

b = 3;
c = 2;
console.log(b > c); //true
console.log(b < c); //false
console.log(b >= c); //true
console.log(b <= c); //false

b = "4";
c = 4;
console.log(b != c); //false
console.log(b !== c); //true

// Logical Operators
console.log(!true); //false
console.log(true && false); //false
console.log(true || false); //true
